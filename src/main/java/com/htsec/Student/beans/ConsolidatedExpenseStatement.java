public class ConsolidatedExpenseStatement {
    private String branchBankRunCost;
    private String subbranchRunCost;
    private String subbranchBuildCost;
    private String teamBuildCost;
    private String teamWages;
    private String channelBuildCost;
    private String channelRunCost;
    private String marketingCost;

    public String getBranchBankRunCost() {
        return branchBankRunCost;
    }

    public void setBranchBankRunCost(String branchBankRunCost) {
        this.branchBankRunCost = branchBankRunCost;
    }

    public String getSubbranchRunCost() {
        return subbranchRunCost;
    }

    public void setSubbranchRunCost(String subbranchRunCost) {
        this.subbranchRunCost = subbranchRunCost;
    }

    public String getSubbranchBuildCost() {
        return subbranchBuildCost;
    }

    public void setSubbranchBuildCost(String subbranchBuildCost) {
        this.subbranchBuildCost = subbranchBuildCost;
    }

    public String getTeamBuildCost() {
        return teamBuildCost;
    }

    public void setTeamBuildCost(String teamBuildCost) {
        this.teamBuildCost = teamBuildCost;
    }

    public String getTeamWages() {
        return teamWages;
    }

    public void setTeamWages(String teamWages) {
        this.teamWages = teamWages;
    }

    public String getChannelBuildCost() {
        return channelBuildCost;
    }

    public void setChannelBuildCost(String channelBuildCost) {
        this.channelBuildCost = channelBuildCost;
    }

    public String getChannelRunCost() {
        return channelRunCost;
    }

    public void setChannelRunCost(String channelRunCost) {
        this.channelRunCost = channelRunCost;
    }

    public String getMarketingCost() {
        return marketingCost;
    }

    public void setMarketingCost(String marketingCost) {
        this.marketingCost = marketingCost;
    }
}
