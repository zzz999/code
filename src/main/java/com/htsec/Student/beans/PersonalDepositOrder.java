package com.htsec.Student.beans;

public class PersonalDepositOrder {
    private String ADmoney;
    private String orderRate;
    private String name;
    private String code;

    public String getADmoney() {
        return ADmoney;
    }

    public void setADmoney(String ADmoney) {
        this.ADmoney = ADmoney;
    }

    public String getOrderRate() {
        return orderRate;
    }

    public void setOrderRate(String orderRate) {
        this.orderRate = orderRate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
