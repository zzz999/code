package com.htsec.service.dto;

public class ComprehensivePerformance {
    private String assetAllocation;
    private String operate;
    private String stockSelection;
    private String stability;
    private String marketTiming;
    private String riskControl;

    public String getAssetAllocation() {
        return assetAllocation;
    }

    public void setAssetAllocation(String assetAllocation) {
        this.assetAllocation = assetAllocation;
    }

    public String getOperate() {
        return operate;
    }

    public void setOperate(String operate) {
        this.operate = operate;
    }

    public String getStockSelection() {
        return stockSelection;
    }

    public void setStockSelection(String stockSelection) {
        this.stockSelection = stockSelection;
    }

    public String getStability() {
        return stability;
    }

    public void setStability(String stability) {
        this.stability = stability;
    }

    public String getMarketTiming() {
        return marketTiming;
    }

    public void setMarketTiming(String marketTiming) {
        this.marketTiming = marketTiming;
    }

    public String getRiskControl() {
        return riskControl;
    }

    public void setRiskControl(String riskControl) {
        this.riskControl = riskControl;
    }
}
